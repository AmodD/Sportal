<div class="content box">
 <p><strong>Tournament</strong> is a competition between/amongst participants for a specific sport format</p>
 <p><strong>Season</strong> is typically the year of the tournament </p>
 <p><strong>Event</strong> is a contest in a tournament's season within a particular type and category  </p>
 <p><strong>Participant</strong> is a polymorphic concept with 2 types namely 'player' and 'team' </p>
 <p><strong>Player</strong> is an individual human </p>
 <p><strong>Team</strong> is a group of atleast two players</p>

